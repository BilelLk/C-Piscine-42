int	ft_sqrt(int nb)
{
	int	y;
	int	racine_max;

	y = 1;
	racine_max = 46340;
	while (y <= racine_max)
	{
		if ((y * y) == nb)
			return (y);
		y++;
	}
	return (0);
}
